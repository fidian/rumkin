<?PHP Section('Preparation'); ?>

<ol>

<li>Take a couple cups of water and use the wisk, electric mixer, or paint
mixer to mix
in the gelatin as well as possible.

<li>Heat water to 130&deg; F &plusmn; 10&deg; F (54.4&deg; C &plusmn; 5.6&deg; C)
in the very large pot.  There's going to be
foam, so make sure that you have room to spare.

<li>Add in the gelatin goo.  Add it in slowly and mix it well.

<li>Put a layer of paper towels on the mesh to make a filter.

<li>Use a large spoon to scoop off foam.  Dispose of in the sink with
running hot water.

<li>Coat mold with silicone spray to make removal easier.

<li>Put the filter over your mold.  Either get it rigged up to stay there or
get a friend to hold it.

<li>Pour the gelatin into the mold through the filter to remove the foam and
lumps.

<li>Let stand for 4 hours at room temperature, with aluminum foil tented
over the mold to prevent airborne contaminants.

<li>Place into refrigerator at 39&deg; F (3.9 &deg; C) for 24 hours or more.

<li>Remove gelatin from mold.  Pour a small amount of ice cold water between
the mold and the gelatin to ease removal.  Running warm water on the outside 
of the mold will help out if it remains stuck.

<li>Blot water from block using paper towels.

<li>Wrap gelatin in a plastic bag.

<li>Return to 39&deg; F (3.9 &deg; C) refrigerator and wait at least another 24 hours.

</ol>

<p>Use a cooler to transport the gelatin.  Do not add ice otherwise it may
skew the results.  Shoot a steel BB into it at a velocity of 590 fps
&plusmn; 30 fps (180 m/s &plusmn; 9 m/s).  It should penetrate 3.35 inches
(8.5 cm).  If you don't shoot at the right speed, it will penetrate just a bit
more or less, so a tiny amount of variation is acceptable.

