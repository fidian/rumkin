<p>Over the years, I have been asked why I selected "rumkin.com" as my site's
name.  I repeatedly answer them with a fragment here and a sentence there.
This page is a collection of everything rumkin-esque.</p>
	
<?php Section('The Hunt'); ?>
	
<p>Before I registered my own domain name, I had a few rules in mind.  I
wanted it short, pronounceable, a word, non-offensive, and hard to screw up.
I did not want anything with a number, symbol, nor anything that could be 
spelled multiple ways if I were to say the site name to someone.  Things like
"4ever.com" and "be-good.com" were instantly removed from consideration.
I also wanted it to be 6 letters or less and I wanted a "dot com" ending.</p>

<p>I wrote a program that took a wordlist and checked with DNS servers and
various WHOIS servers in order to see if that domain was currently
registered.  This generated a list of possible domain names.</p>

<p>At that time (February 22, 2001), I was able to find a single 4 letter
domain name:  rynt.com.  It did not satisfy my requirement of being
pronounceable, and if I were to say "rynt.com" to people, they might try to
type rint.com.</p>

<p>The search continued and 778 entries with 5 letters and 3791 with 6
letters.  I painstakingly went through each entry by hand and proposed only
the very best to my fiance&eacute; (who later became my wife).  We settled
on <tt>rumkin.com</tt> because when someone tells you the site name and
you try to spell it, you usually get it
right.  Well, there are those that add a "p" in there, but there will always
be someone that gets it wrong.</p>

<?php Section('Other Rumkins'); ?>

