<h2>Search for Traps</h2>

<p>I lost count of the number of players that were lost because they didn't
check for traps on a keyhole or in a suspicious hallway.</p>

<h2>Stay Together</h2>

<p>You don't want to split up to explore different levels of a castle.  Stay
together, but not so close that a fireball or a clever room-based trap can
get you all.</p>

<h2>Focus Attacks</h2>

<p>Have your team pick on only a couple monsters at a time instead of each
person fighting one or two separate enemies.  They will die quicker and you
will take less damage because there will be less of them hitting you later
in the battle.</p>

<h2>Always Have An Escape Plan; Sometimes Use It</h2>

<p>If nothing else, have one person stay way in back so they will be able to
run to town to gather a party to avenge your deaths.</p>

<h2>Secret Doors</h2>

<p>Look at suspicious hallways going nowhere, closets when there is no
reason for a closet, passages between rooms, and other secret doors in a
secret room.</p>

<h2>Search Stomachs of Large Monsters</h2>

<p>Really, this rule is "If a monster can eat someone whole or if they eat
rock, minerals, or gems, then you should search their stomach(s)."</p>

<h2>Check For False Bottoms</h2>

<p>Desk drawers, treasure chests, and even clothing can have secret hiding
spots.  If the bad guy had something to hide, do you think they would leave
it on a chair in the middle of the room with a spotlight on it?</p>

<h2>Alcohol Has Many Uses</h2>

<p>My party thought that this was a good rule to have on the list, but I
didn't.  In case you were wondering, we used alcohol to get drunk, to get
others drunk, to sterilize wounds, to start fires (we carried the <i>good
stuff</i>), as a solvent or thinner, to kill bacteria in questionable
liquids, as a bribe or payment, and as a counterweight.  I'm sure that I am
missing a few of our uses, but life wasn't dull when one party member kept a
small barrel of alcohol around.
	
